    public InformacionUsuario() {
        initComponents();
        usuario = Login.usuario;
        usuario_actualizado = PanelUsuarios.usuario_update;
        tablaUsuarios = PanelUsuarios.Table_listaUsuarios;
        modelo = PanelUsuarios.modelo;
        
        setSize(564, 362);
        setResizable(false); 
        setTitle("Información del usuario " + usuario_actualizado);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(WindowConstants.DO_NOTHING_ON_CLOSE);
        
        Fila = tablaUsuarios.getSelectedRow();
        
        //Hace la consulta de la clave y el id a la base de datos
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                "select Id_usuario, Usuario, Clave from usuarios where Nombres = '" + usuario_actualizado + "'");
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                ID = rs.getInt("Id_usuario");
                lbl_usuario.setText(rs.getString("Usuario"));
                lbl_passUsuario.setText(rs.getString("Clave"));
            }  
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error en cargar usuario. " + e);
            JOptionPane.showMessageDialog(null, "¡¡ERROR al cargar!!, contacte al administrador.");
        }
        
        //Traemos los datos del Jtable del panel Usuarios
        lbl_nombreUsuario.setText(tablaUsuarios.getValueAt(Fila, 0).toString());
        lbl_apellidoUsuario.setText(tablaUsuarios.getValueAt(Fila, 1).toString());
        lbl_celularUsuario.setText(tablaUsuarios.getValueAt(Fila, 2).toString());
        lbl_correoUsuario.setText(tablaUsuarios.getValueAt(Fila, 3).toString());
        lbl_rolUsuario.setText(tablaUsuarios.getValueAt(Fila, 5).toString());
        
    }
    
