    //Metodo boton Eliminar
    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        btn_eliminar.setEnabled(false);
        int decision = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea eliminar?", "Eliminar usuario", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if(decision == JOptionPane.YES_OPTION){    
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("delete from usuarios where Nombres= '"+ usuario_actualizado + "'");
                pst.executeUpdate(); 
                
                int filaSelec = tablaUsuarios.getSelectedRow();
                String nombre = tablaUsuarios.getValueAt(filaSelec, 0).toString();
                
                modelo.removeRow(filaSelec);
                    
                JOptionPane.showMessageDialog(null, "El usuario: " + usuario_actualizado + " ha sido eliminado.");            
                btn_eliminar.setEnabled(true);
                dispose();
           
            } catch (SQLException e) {
                System.err.println("Error al eliminar usuario. " + e);
                JOptionPane.showMessageDialog(null, "¡¡ERROR al eliminar!!, contacte al administrador.");
            }
                    
        }if(decision == JOptionPane.NO_OPTION){
             btn_eliminar.setEnabled(true);
        }        
            
    }//GEN-LAST:event_btn_eliminarActionPerformed

