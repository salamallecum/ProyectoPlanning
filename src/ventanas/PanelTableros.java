    public PanelTableros() {
        initComponents();
        
        TableColumnModel columnModel = Table_listaTableros.getColumnModel();

        columnModel.getColumn(0).setPreferredWidth(70);
        columnModel.getColumn(1).setPreferredWidth(30);
        
        
        //Traemos el contenidode la tabla tableros de la BD
        try {
            modelo = new DefaultTableModel();
            Table_listaTableros.setModel(modelo);
            
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                        "select Nombre_tablero, Tipo from tableros");
            
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsmd = rs.getMetaData();
            int cantidadColumnas = rsmd.getColumnCount();
            
            modelo.addColumn("Nombre");
            modelo.addColumn("Tipo");
                                  
            while (rs.next()) {
                
                Object[] filas = new Object[cantidadColumnas];
                
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
                }
                cn.close();        
            
            } catch (SQLException e) {
                System.err.println("Error al llenar tabla." + e);
                JOptionPane.showMessageDialog(null, "Error al mostrar información, ¡Contacte al administrador!");
            }

            //Agregamos la funcion de ver informacion del cliente al hacer click sobre el registro de la tabla
            Table_listaTableros.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e){
                int fila_point = Table_listaTableros.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if(fila_point > -1){
                    tablero_update = (String) modelo.getValueAt(fila_point, columna_point);
                    DetalleTablero detalle_tablero = new DetalleTablero();
                    detalle_tablero.setVisible(true);
                }

            }
        });
    }

