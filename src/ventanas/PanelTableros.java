       //Metodo que busca un criterio de busqueda de propietario y carga resultados mientras uno escribe
    public void busquedaTablero(String texto){
        try{
            String [] titulos = {"Nombre", "Tipo"};
            String filtro = ""+texto+"_%";
            String SQL = "select Nombre_tablero, Tipo from tableros where Nombre_tablero like "+'"'+filtro+'"';
            modelo = new DefaultTableModel(null, titulos);
            
            Connection cn6 = Conexion.conectar();
            PreparedStatement pst6 = cn6.prepareStatement(SQL);
            ResultSet rs6 = pst6.executeQuery(SQL);
            
            String[] fila = new String[6];
            
            while(rs6.next()){
                fila[0]=rs6.getString("Nombre_tablero");
                fila[1]=rs6.getString("Tipo");
               
                modelo.addRow(fila);
            }
            Table_listaTableros.setModel(modelo);
            rs6.close();
            cn6.close();
            
        }catch(Exception ex){
            System.err.println("Error en busqueda de tablero." + ex);
            JOptionPane.showMessageDialog(null, "¡¡ERROR de busqueda!!, contacte al administrador.");
        }
    }    




