    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        
        int roles_cmb, validacion = 0;
        String nombres, apellidos, celular, correo, usuario, pass, roles_string = "";
        
        nombres = txt_nombres.getText().trim();
        apellidos = txt_apellidos.getText().trim();
        celular = txt_celular.getText().trim();
        correo = txt_correo.getText().trim();
        usuario = txt_nombreUsuario.getText().trim();
        pass = txt_clave.getText().trim();
        roles_cmb = cmb_Rol.getSelectedIndex() + 1;
        
        if(nombres.equals("")){
            txt_nombres.setBackground(Color.red);
            validacion++;
        }
        if(apellidos.equals("")){
            txt_apellidos.setBackground(Color.red);
            validacion++;
        }
        if(celular.equals("")){
            txt_celular.setBackground(Color.red);
            validacion++;
        }
        if(correo.equals("")){
            txt_correo.setBackground(Color.red);
            validacion++;
        }
        if(usuario.equals("")){
            txt_nombreUsuario.setBackground(Color.red);
            validacion++;
        }
        if(pass.equals("")){
            txt_clave.setBackground(Color.red);
            validacion++;
        }   
        
        if(roles_cmb == 1){
            roles_string = "Administra";
        } else if(roles_cmb == 2){
            roles_string = "Usuario";
        } 
         
        if (validacion == 0) {
            
            FilaAnterior = tablaUsuarios.getSelectedRow();
            String nombre = tablaUsuarios.getValueAt(FilaAnterior, 0).toString();
            
            try{
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("update usuarios set Nombres ='"+txt_nombres.getText()+"',Apellidos='"+txt_apellidos.getText()+"',Celular='"+txt_celular.getText()+"',Correo='"+txt_correo.getText()+"',"
                   +"Clave='"+txt_clave.getText()+"',Rol='"+roles_string+"'where id_usuario ='"+ID+"'");

                pst.executeUpdate();
                cn.close();
                
                Object Fila[] = new Object[6];
                Fila[0] = txt_nombres.getText();
                Fila[1] = txt_apellidos.getText();
                Fila[2] = txt_celular.getText();
                Fila[3] = txt_correo.getText();
                Fila[4] = txt_nombreUsuario.getText();
                Fila[5] = roles_string;

                modelo.addRow(Fila);
                modelo.removeRow(FilaAnterior);
                
                System.out.println("Usuario actualizado satisfactoriamente");
                JOptionPane.showMessageDialog(null, "Usuario actualizado satisfactoriamente.");
                InformacionUsuario.btn_editar.setEnabled(true);
                this.dispose();
                

            }catch(SQLException e){
                System.err.println("Error al Actualizar el usuario." + e);
                        JOptionPane.showMessageDialog(null, "¡¡ERROR al actualizar!!, contacte al administrador.");

            }    
        
        } else {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos.");
            Normalizar();
        } 
            
    }//GEN-LAST:event_btn_actualizarActionPerformed

