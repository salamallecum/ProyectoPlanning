    public EditarUsuario() {
        initComponents();
        usuario = Login.usuario;
        usuario_actualizado = PanelUsuarios.usuario_update;
        tablaUsuarios = PanelUsuarios.Table_listaUsuarios;
        modelo = PanelUsuarios.modelo;
        
        setSize(562,396);
        setResizable(false);
        setTitle("Editar usuario ");
        setLocationRelativeTo(null);
        setDefaultCloseOperation(WindowConstants.DO_NOTHING_ON_CLOSE);
              
        //Hace la consulta de registros a la base de datos
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                "select * from usuarios where Nombres = '" + usuario_actualizado + "'");
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                ID = rs.getInt("Id_usuario");
                
                txt_nombres.setText(rs.getString("Nombres"));
                txt_apellidos.setText(rs.getString("Apellidos"));
                txt_celular.setText(rs.getString("Celular"));
                txt_correo.setText(rs.getString("Correo"));
                txt_nombreUsuario.setText(rs.getString("Usuario"));
                txt_clave.setText(rs.getString("Clave"));
                cmb_Rol.setSelectedItem(rs.getString("Rol"));
            }
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error en cargar usuario. " + e);
            JOptionPane.showMessageDialog(null, "¡¡ERROR al cargar!!, contacte al administrador.");
        }
    }
    
